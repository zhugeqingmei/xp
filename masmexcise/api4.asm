;这是一个自己课堂上的例子，在里面使用了变量。
;计算Z=((X+Y)*8-X)/2运算结果不超过16位

ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	VARX DW 0006H
	VARY DW 0007H
	RESULT DW ?
DATA ENDS

CODE SEGMENT

START:
		MOV AX,DATA
		MOV DS,AX
		MOV BX,VARX
		ADD BX,VARY
		MOV CL,3
		SAL BX,CL
		SUB BX,VARX
		SAR BX,1
		MOV RESULT,BX

		MOV AX,4C00H
		INT 21H

CODE ENDS
END START